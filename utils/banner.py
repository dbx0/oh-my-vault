def print_banner(color=None, banner=None): 

    if not color:
        color = random.choice(list(COLORS))
    
    if not banner:
        banner = random.choice(banners)
    else:
        banner = banners[banner]

    print_colored(banner, color=color)

    print_colored(" " * 10 + developer_message, COLORS.RED)
    print_colored(legal_disclaimer, COLORS.YELLOW)
    print_bold("[!] Press Ctrl + C at any time to exit")